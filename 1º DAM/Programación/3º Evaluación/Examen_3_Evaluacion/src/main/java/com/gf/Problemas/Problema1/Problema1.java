    private void creacionControles() {
        //Crea un Array y defino las opciones que va a ver para ponerlo en el JOptionPane
        String[] opcionesTipo = {"FlowLayout", "BorderLayout"};
        //Defini la variable opciones que contiene los tipo de opciones que apareceran
        String opciones = (String) JOptionPane.showInputDialog(null, null, "Problema1", 1, null, opcionesTipo, opcionesTipo[0]);
        /*Si se selecciona la opcion de  tipo [0] "Layout" se le aparecera otro cuardo de dialogo
        que le pedira los numeros */

        if (opciones.equals(opcionesTipo[0])) {

            String botonesAelegir = JOptionPane.showInputDialog(null, "Numero de botones", "FlowLayout", 1);
//Creo un ArrayList donde se almacenaran los numeros

            ArrayList<Integer> numeros = new ArrayList<>();
            // Bucle for para a√±adir al ArrayList de numeros i
            for (int i = 0; i <= 10; i++) {
                numeros.add(i);
            }

            //Agrego el boton el resultado de i
            for (int i = 0; i <= 10; i++) {
                int num = numeros.get(i);
                boton = new JButton("" + num);
                panelContenedor.add(boton);
            }
            
        } else if (opciones.equals(opcionesTipo[1])) {
            this.setLocationRelativeTo(null);
            this.setTitle("BorderLayout");
            //Establezco una nueva dimension para la nueva ventana
            this.setPreferredSize(new Dimension(20, 50));
//Creacion de los JCheckBox
            JCheckBox cajaN = new JCheckBox();
            cajaN.setText("Norte");

            JCheckBox cajaS = new JCheckBox();
            cajaS.setText("Sur");

            JCheckBox cajaE = new JCheckBox();
            cajaE.setText("Este");

            JCheckBox cajaO = new JCheckBox();
            cajaO.setText("Oeste");

            JCheckBox cajaC = new JCheckBox();
            cajaC.setText("Centro");

            panelContenedor.add(cajaN);
            panelContenedor.add(cajaS);
            panelContenedor.add(cajaE);
            panelContenedor.add(cajaO);
            panelContenedor.add(cajaC);
            this.setContentPane(panelContenedor);
            
            if(cajaN.isSelected()){
                JPanel panel = new JPanel();
                panel.add(cajaC);
            }
            
            
        }

    }

