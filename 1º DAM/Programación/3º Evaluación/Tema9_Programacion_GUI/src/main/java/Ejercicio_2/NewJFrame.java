    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
         JFileChooser jfc = new JFileChooser(".\\src\\main\\java\\com\\gf\\imagenes");
        jfc.setDialogTitle("Selecciona la imagen");
        jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        jfc.setMultiSelectionEnabled(true);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de imagen", "jpg", "jpeg", "png");
        jfc.addChoosableFileFilter(filtro);

        int resp = jfc.showDialog(this, "Seleccionar Imagenes");
        if (resp == JFileChooser.APPROVE_OPTION) {
            this.archivoImagen = jfc.getSelectedFiles();
            t.start();
        }
      
    }//GEN-LAST:event_jMenuItem1ActionPerformed

