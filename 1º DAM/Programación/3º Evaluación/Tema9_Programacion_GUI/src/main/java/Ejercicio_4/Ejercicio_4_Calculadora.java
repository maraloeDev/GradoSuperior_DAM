    private void TextoNumero1FocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TextoNumero1FocusLost
        // En el FocusLost, si se sale del foco, el valor por defecto, es 0
        if (this.TextoNumero1.getText().isEmpty()) {
            this.TextoNumero1.setText(String.valueOf(0));
        } else { //controlo que es numérico
            try {
                Double.parseDouble(this.TextoNumero1.getText());
                this.TextoNumero1.setToolTipText(null);
            } catch (NumberFormatException ex) {
                this.TextoNumero1.requestFocus();
                this.TextoNumero1.setToolTipText("Debe introducir un dato numérico");
            }
        }
    }//GEN-LAST:event_TextoNumero1FocusLost

