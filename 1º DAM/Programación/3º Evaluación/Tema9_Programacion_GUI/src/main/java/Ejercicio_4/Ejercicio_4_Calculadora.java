    private void btnSumaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSumaMouseClicked
        /* En este caso, agrego un evento 'MouseClicked', para todos los botones
           establezco en el teztoResultado, los metodos que implemento en la clase Calculadora.java con todas las operaciones*/
        try {
            double numero1 = Double.parseDouble(this.TextoNumero1.getText());
            double numero2 = Double.parseDouble(this.TextoNumero2.getText());
            TextoResultado.setText(String.valueOf(Calculadora.sumarNumeros(numero1, numero2)));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Debe introducir datos numéricos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSumaMouseClicked

