    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser jfc = new JFileChooser("C:\\Users\\maraloed\\Pictures\\Camera Roll");
        jfc.setDialogTitle("Selecciona la imagen");
        jfc.setFileSelectionMode(JFileChooser.FILES_ONLY);
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivos de imagen", "jpg", "jpeg", "png");
        jfc.addChoosableFileFilter(filtro);
        File archivoImagen;

        int resp = jfc.showOpenDialog(this);
        if (resp == JFileChooser.APPROVE_OPTION) {
            archivoImagen = jfc.getSelectedFile();
            //Colocamos la imagen en la etiqueta
            //this.jLabel1.setIcon(new ImageIcon(archivoImagen.getPath()));
            //Escalamos la imagen al tama√±o de la etiqueta contenedora
            ImageIcon imagen = new ImageIcon(archivoImagen.getPath());
            Image imagenEscalada = imagen.getImage().getScaledInstance(this.jLabel1.getWidth(), this.jLabel1.getHeight(), Image.SCALE_SMOOTH);
            this.jLabel1.setIcon(new ImageIcon(imagenEscalada));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

